export function getFixedScheduleForWeek(weekNumber: number): ScheduleGroup[] {
  switch (weekNumber) {
    case 1: return WEEK1_SCHEDULE;
    case 2: return WEEK2_SCHEDULE;
    case 3: return WEEK3_SCHEDULE;
    case 4: return WEEK4_SCHEDULE;
    default: return [];
  }
}

export function getAllFixedCompanyNames(): string[] {
  const names = new Set<string>();
  [...WEEK1_SCHEDULE, ...WEEK2_SCHEDULE, ...WEEK3_SCHEDULE, ...WEEK4_SCHEDULE].forEach(g => g.companies.forEach(c => names.add(c)));
  return Array.from(names);
}

export function isMainCompany(companyName: string): boolean {
  return [...WEEK1_SCHEDULE, ...WEEK2_SCHEDULE, ...WEEK3_SCHEDULE, ...WEEK4_SCHEDULE].some(g => g.companies[0] === companyName);
}
